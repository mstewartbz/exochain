#[cfg(test)]
mod tests {
    use super::*;

    fn make_results() -> Vec<InvariantResult> {
        (1..=9)
            .map(|i| InvariantResult {
                invariant_id: InvariantId::new(&format!("INV-{:03}", i)),
                satisfied: true,
                reduction_steps: 1,
                message: "ok".to_string(),
            })
            .collect()
    }

    #[test]
    fn test_valid_proof_creation() {
        let results = make_results();
        let reg_hash = Blake3Hash([1u8; 32]);
        let kern_hash = Blake3Hash([2u8; 32]);
        let proof = CgrProof::new(1, &results, &reg_hash, &kern_hash, 1000);
        assert_eq!(proof.status, ProofStatus::Valid);
        assert_eq!(proof.invariants_checked, 9);
        assert_eq!(proof.total_reduction_steps, 9);
        assert!(proof.verify_integrity());
    }

    #[test]
    fn test_proof_hash_deterministic() {
        let results = make_results();
        let reg_hash = Blake3Hash([1u8; 32]);
        let kern_hash = Blake3Hash([2u8; 32]);
        let p1 = CgrProof::new(1, &results, &reg_hash, &kern_hash, 1000);
        let p2 = CgrProof::new(1, &results, &reg_hash, &kern_hash, 1000);
        assert_eq!(p1.proof_hash, p2.proof_hash);
    }

    #[test]
    fn test_proof_hash_changes_with_id() {
        let results = make_results();
        let reg_hash = Blake3Hash([1u8; 32]);
        let kern_hash = Blake3Hash([2u8; 32]);
        let p1 = CgrProof::new(1, &results, &reg_hash, &kern_hash, 1000);
        let p2 = CgrProof::new(2, &results, &reg_hash, &kern_hash, 1000);
        assert_ne!(p1.proof_hash, p2.proof_hash);
    }

    #[test]
    fn test_rejected_proof() {
        let mut results = make_results();
        results[0].satisfied = false;
        let proof = CgrProof::new(1, &results, &Blake3Hash([0u8; 32]), &Blake3Hash([0u8; 32]), 1000);
        assert_eq!(proof.status, ProofStatus::Rejected);
    }

    #[test]
    fn test_covers_invariant() {
        let results = make_results();
        let proof = CgrProof::new(1, &results, &Blake3Hash([0u8; 32]), &Blake3Hash([0u8; 32]), 1000);
        assert!(proof.covers_invariant(&InvariantId::new("INV-001")));
        assert!(proof.covers_invariant(&InvariantId::new("INV-009")));
        assert!(!proof.covers_invariant(&InvariantId::new("INV-999")));
    }

    #[test]
    fn test_from_kernel() {
        let results = make_results();
        let kern_hash = Blake3Hash([42u8; 32]);
        let proof = CgrProof::new(1, &results, &Blake3Hash([0u8; 32]), &kern_hash, 1000);
        assert!(proof.from_kernel(&kern_hash));
        assert!(!proof.from_kernel(&Blake3Hash([99u8; 32])));
    }

    #[test]
    fn test_tampered_proof_detected() {
        let results = make_results();
        let mut proof = CgrProof::new(1, &results, &Blake3Hash([0u8; 32]), &Blake3Hash([0u8; 32]), 1000);
        assert!(proof.verify_integrity());
        // Tamper
        proof.timestamp_ms = 9999;
        assert!(!proof.verify_integrity());
    }
}
